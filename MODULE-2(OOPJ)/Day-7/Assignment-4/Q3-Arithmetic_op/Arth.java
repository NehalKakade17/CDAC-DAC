package homework;

public class Arth {

	public static void main(String[] args) {
				int intValue = 10;
		        float floatValue = 20.5f;
		        double doubleValue = 30.75;
		        double additionResult = intValue + floatValue + doubleValue;
		        System.out.println("Addition Result (int + float + double): " + additionResult);
		        double subtractionResult = doubleValue - floatValue - intValue;
		        System.out.println("Subtraction Result (double - float - int): " + subtractionResult);
		        double multiplicationResult = intValue * floatValue * doubleValue;
		        System.out.println("Multiplication Result (int * float * double): " + multiplicationResult);
		        double divisionResult = doubleValue / floatValue / intValue;
		        System.out.println("Division Result (double / float / int): " + divisionResult);
		    }
		


	

}
